//WAJP to check the alphabet is special character or not
class SpecialChar
{
	public static void main(String[] args)
	{
		char c = '@' ;
		if(c>=97 && c<=122 || c>=65 && c<=90 || c>=48 && c<=57 )
		{
		System.out.println("Not Special Character");
		}
		else
		{
		System.out.println("Special Character");
		}
	}
}

	

